    {
        //
        $AuthUser = User::where('usuario',$request['user'])
        ->where('clave',md5($request['password']))
        ->get();
       // dd($request['user'], $request['password'], $AuthUser);
        if(count($AuthUser)>0){
            Auth::login($AuthUser[0],true);
             return Redirect::to('/filtros');
        }

         return  Redirect::to('/');
    }

